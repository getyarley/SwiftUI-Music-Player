import Foundation
import SwiftUI

extension Color {
    static let yellowRed = Color("YellowRed")
    static let charcoal = Color("Charcoal")
    static let lightGray = Color("LightGray")
    static let silver = Color("Silver")
}

extension UIColor {
    static let yellowRed = UIColor(named: "YellowRed")
    static let charcoal = UIColor(named: "Charcoal")
    static let lightGray = UIColor(named: "LightGray")
    static let silver = UIColor(named: "Silver")
}
